import os
import platform
import sys

from dotenv import load_dotenv

load_dotenv()

print(f'DEBUG: Starting on {platform.platform()}', file=sys.stderr)

if os.name != 'nt':
    os.system(
        f'gunicorn -w 9 -k uvicorn.workers.UvicornWorker -b 0.0.0.0:{os.getenv("PORT", "5000")} main:app'
    )
else:
    os.system(
        f'uvicorn --host 0.0.0.0 --port {os.getenv("PORT", "5000")} --backlog 20000 '
        '--no-server-header --workers 1 --use-colors --access-log --log-level debug main:app'
    )
